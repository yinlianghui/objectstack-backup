export function parseCsv(text) {
  const rows = [];
  let row = [];
  let cell = '';
  let quoted = false;
  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];
    if (quoted && char === '"' && text[index + 1] === '"') {
      cell += '"';
      index += 1;
    } else if (char === '"') {
      quoted = !quoted;
    } else if (char === ',' && !quoted) {
      row.push(cell);
      cell = '';
    } else if ((char === '\n' || char === '\r') && !quoted) {
      if (char === '\r' && text[index + 1] === '\n') index += 1;
      row.push(cell);
      if (row.some((value) => value !== '')) rows.push(row);
      row = [];
      cell = '';
    } else {
      cell += char;
    }
  }
  if (cell !== '' || row.length > 0) {
    row.push(cell);
    rows.push(row);
  }
  const [headers = [], ...values] = rows;
  return values.map((valueRow) => Object.fromEntries(
    headers.map((header, index) => [header, valueRow[index] ?? '']),
  ));
}

async function readCsv(dataDir, name) {
  return parseCsv(await fs.readFile(path.join(dataDir, name), 'utf8'));
}

const itemRow = (row) => ({
  external_key: row.external_key,
  name: row.name,
  amount: Number(row.amount),
  active: row.active === 'true',
});

const childRow = (row) => ({
  external_key: row.external_key,
  name: row.name,
  parent_id: row.parent_id,
  amount: Number(row.amount),
});

export async function buildSeedGroups(dataDir) {
  const [parents, seedItems, importItems, singleChildren, tenChildren] = await Promise.all([
    readCsv(dataDir, 'qa_summary_parents.csv'),
    readCsv(dataDir, 'qa_seed_item.csv'),
    readCsv(dataDir, 'qa_import_item.csv'),
    readCsv(dataDir, 'qa_summary_child_single_parent.csv'),
    readCsv(dataDir, 'qa_summary_child_ten_parents.csv'),
  ]);
  return {
    parents: parents.map(({ id, name }) => ({ id, name })),
    seedItems: seedItems.map(itemRow),
    importItems: importItems.map(itemRow),
    singleChildren: singleChildren.map(childRow),
    tenChildren: tenChildren.map(childRow),
  };
}

export function renderObjectsModule(canonicalSource) {
  const legacyTenancy = 'tenancy: { enabled: false },';
  const matches = canonicalSource.split(legacyTenancy).length - 1;
  if (matches !== 4) {
    throw new Error(`Expected four legacy disabled-tenancy declarations; found ${matches}.`);
  }
  let normalized = canonicalSource.replaceAll(
    legacyTenancy,
    'tenancy: { enabled: false, strategy: "shared" },',
  );
  const sharingModels = {
    qa_seed_item: 'public_read_write',
    qa_import_item: 'public_read_write',
    qa_summary_parent: 'public_read_write',
    qa_summary_child: 'controlled_by_parent',
  };
  for (const [name, sharingModel] of Object.entries(sharingModels)) {
    const pattern = new RegExp(`(name: "${name}",\\n    label: "[^"]+",)`);
    if (!pattern.test(normalized)) throw new Error(`Unable to add sharingModel to ${name}.`);
    normalized = normalized.replace(pattern, `$1\n    sharingModel: "${sharingModel}",`);
  }
  return [
    '// GENERATED from ../../source/object-definitions.ts by scripts/build-artifacts.mjs.',
    '// Current protocol normalizations: disabled tenancy uses strategy "shared" and OWD is explicit.',
    '// Do not edit this copy by hand.',
    '',
    normalized,
  ].join('\n');
}

export function renderSeedModule(groups) {
  const parentSeed = [{
    object: 'qa_summary_parent',
    mode: 'insert',
    records: groups.parents,
  }];
  const fullSeed = [
    ...parentSeed,
    { object: 'qa_seed_item', mode: 'insert', records: groups.seedItems },
    { object: 'qa_import_item', mode: 'insert', records: groups.importItems },
    {
      object: 'qa_summary_child',
      mode: 'insert',
      records: [...groups.singleChildren, ...groups.tenChildren],
    },
  ];
  return [
    '// GENERATED from the archived CSV fixtures by scripts/build-artifacts.mjs.',
    '// Do not edit this file by hand.',
    `export const parentSeed = ${JSON.stringify(parentSeed, null, 2)};`,
    `export const fullSeed = ${JSON.stringify(fullSeed, null, 2)};`,
    '',
  ].join('\n');
}

export async function resolveExcelJs(frameworkRoot) {
  const requireFromRest = createRequire(path.join(frameworkRoot, 'packages/rest/package.json'));
  const loaded = requireFromRest('exceljs');
  return loaded.default ?? loaded;
}

export async function resolveJsZip(frameworkRoot) {
  const requireFromRest = createRequire(path.join(frameworkRoot, 'packages/rest/package.json'));
  const excelPackage = requireFromRest.resolve('exceljs/package.json');
  const requireFromExcel = createRequire(excelPackage);
  const loaded = requireFromExcel('jszip');
  return loaded.default ?? loaded;
}

export async function writeSingleSheetWorkbook(ExcelJS, JSZip, destination, sheetName, rows) {
  if (rows.length === 0) throw new Error(`Cannot build empty workbook: ${destination}`);
  const headers = Object.keys(rows[0]);
  const workbook = new ExcelJS.Workbook();
  const fixedDate = new Date('2026-07-17T00:00:00.000Z');
  workbook.creator = 'ObjectStack Issue 2678 QA';
  workbook.created = fixedDate;
  workbook.modified = fixedDate;
  const worksheet = workbook.addWorksheet(sheetName);
  worksheet.views = [{ state: 'frozen', ySplit: 1 }];
  worksheet.addRow(headers);
  worksheet.getRow(1).font = { bold: true };
  for (const row of rows) worksheet.addRow(headers.map((header) => row[header]));
  worksheet.columns.forEach((column) => { column.width = 22; });
  const zip = await JSZip.loadAsync(Buffer.from(await workbook.xlsx.writeBuffer()));
  for (const entry of Object.values(zip.files)) entry.date = fixedDate;
  const deterministic = await zip.generateAsync({
    type: 'nodebuffer',
    compression: 'DEFLATE',
    compressionOptions: { level: 6 },
    platform: 'UNIX',
  });
  await fs.mkdir(path.dirname(destination), { recursive: true });
  await fs.writeFile(destination, deterministic);
}

function requiredFrameworkRoot(argv, env = process.env) {
  const index = argv.indexOf('--framework-root');
  const value = index >= 0 ? argv[index + 1] : env.FRAMEWORK_ROOT;
  if (!value) throw new Error('Provide --framework-root or FRAMEWORK_ROOT.');
  if (index >= 0 && !value) throw new Error('--framework-root requires a value.');
  return path.resolve(value);
}

function gitOutput(frameworkRoot, args) {
  const result = spawnSync('git', ['-C', frameworkRoot, ...args], { encoding: 'utf8' });
  if (result.status !== 0) {
    throw new Error(`git ${args.join(' ')} failed: ${result.stderr?.trim()}`);
  }
  return result.stdout.trim();
}

async function sha256(file) {
  return crypto.createHash('sha256').update(await fs.readFile(file)).digest('hex');
}

function frameworkCliVersion(frameworkRoot) {
  const result = spawnSync(process.execPath, frameworkCliArgs(frameworkRoot, ['--version']), {
    cwd: frameworkRoot,
    encoding: 'utf8',
  });
  if (result.status !== 0) {
    throw new Error(`Unable to read local Framework CLI version: ${result.stderr?.trim()}`);
  }
  return result.stdout.trim();
}

export function normalizeCompileOutput(output, mode) {
  const { duration: _duration, output: _absoluteOutput, ...stable } = output;
  return {
    ...stable,
    output: `dist/${mode}/objectstack.json`,
  };
}

async function writeGeneratedSources(packageRoot, archiveRoot) {
  const canonical = await fs.readFile(
    path.join(archiveRoot, 'source/object-definitions.ts'),
    'utf8',
  );
  const groups = await buildSeedGroups(path.join(archiveRoot, 'data'));
  const sourceDir = path.join(packageRoot, 'src');
  await fs.mkdir(sourceDir, { recursive: true });
  await Promise.all([
    fs.writeFile(
      path.join(sourceDir, 'objects.generated.ts'),
      renderObjectsModule(canonical),
    ),
    fs.writeFile(
      path.join(sourceDir, 'seed.generated.ts'),
      renderSeedModule(groups),
    ),
  ]);
  return groups;
}

async function buildFixtures(packageRoot, archiveRoot, frameworkRoot, groups) {
  const csvDir = path.join(packageRoot, 'fixtures/csv');
  const xlsxDir = path.join(packageRoot, 'fixtures/xlsx');
  await Promise.all([
    fs.mkdir(csvDir, { recursive: true }),
    fs.mkdir(xlsxDir, { recursive: true }),
  ]);
  const fixtures = [
    ['qa_import_item.csv', 'Import Items', groups.importItems],
    ['qa_summary_child_single_parent.csv', 'Single Parent Children', groups.singleChildren],
    ['qa_summary_child_ten_parents.csv', 'Ten Parent Children', groups.tenChildren],
  ];
  const ExcelJS = await resolveExcelJs(frameworkRoot);
  const JSZip = await resolveJsZip(frameworkRoot);
  for (const [csvName, sheetName, rows] of fixtures) {
    await fs.copyFile(
      path.join(archiveRoot, 'data', csvName),
      path.join(csvDir, csvName),
    );
    await writeSingleSheetWorkbook(
      ExcelJS,
      JSZip,
      path.join(xlsxDir, csvName.replace(/\.csv$/, '.xlsx')),
      sheetName,
      rows,
    );
  }
  await fs.copyFile(
    path.join(archiveRoot, 'data/expected-results.json'),
    path.join(packageRoot, 'expected-results.json'),
  );
}

async function compileArtifact({
  frameworkRoot,
  packageRoot,
  stageRoot,
  configName,
  mode,
  frameworkSha,
  frameworkDirty,
  cliVersion,
}) {
  const outputDir = path.join(packageRoot, 'dist', mode);
  const outputFile = path.join(outputDir, 'objectstack.json');
  await fs.mkdir(outputDir, { recursive: true });
  const command = frameworkCliArgs(frameworkRoot, [
    'compile',
    path.join(stageRoot, configName),
    '--output',
    outputFile,
    '--json',
  ]);
  const result = spawnSync(process.execPath, command, {
    cwd: frameworkRoot,
    encoding: 'utf8',
  });
  if (result.status !== 0) {
    throw new Error([
      `ObjectStack compile failed for ${mode}.`,
      result.stdout?.trim(),
      result.stderr?.trim(),
    ].filter(Boolean).join('\n'));
  }
  const compileOutput = result.stdout.trim().split('\n').filter(Boolean).at(-1);
  const provenance = {
    mode,
    frameworkRepository: 'objectstack-ai/framework',
    frameworkSha,
    frameworkDirty,
    frameworkCli: cliVersion,
    node: process.version,
    artifactSha256: await sha256(outputFile),
    compileOutput: compileOutput
      ? normalizeCompileOutput(JSON.parse(compileOutput), mode)
      : null,
  };
  await fs.writeFile(
    path.join(outputDir, 'build-provenance.json'),
    `${JSON.stringify(provenance, null, 2)}\n`,
  );
}

async function buildArtifacts(frameworkRoot) {
  const scriptPath = fileURLToPath(import.meta.url);
  const packageRoot = path.dirname(path.dirname(scriptPath));
  const archiveRoot = path.dirname(packageRoot);
  const groups = await writeGeneratedSources(packageRoot, archiveRoot);
  await buildFixtures(packageRoot, archiveRoot, frameworkRoot, groups);

  const stageRoot = await fs.mkdtemp(path.join(os.tmpdir(), 'issue-2678-ui-build-'));
  try {
    await fs.cp(path.join(packageRoot, 'src'), path.join(stageRoot, 'src'), { recursive: true });
    await Promise.all([
      fs.copyFile(
        path.join(packageRoot, 'objectstack.config.ts'),
        path.join(stageRoot, 'objectstack.config.ts'),
      ),
      fs.copyFile(
        path.join(packageRoot, 'objectstack.seeded.config.ts'),
        path.join(stageRoot, 'objectstack.seeded.config.ts'),
      ),
      fs.writeFile(path.join(stageRoot, 'package.json'), '{"type":"module"}\n'),
    ]);
    await fs.symlink(
      path.join(frameworkRoot, 'packages/cli/node_modules'),
      path.join(stageRoot, 'node_modules'),
      'dir',
    );
    const frameworkSha = gitOutput(frameworkRoot, ['rev-parse', 'HEAD']);
    const frameworkDirty = gitOutput(frameworkRoot, ['status', '--porcelain']).length > 0;
    const cliVersion = frameworkCliVersion(frameworkRoot);
    for (const [mode, configName] of [
      ['manual', 'objectstack.config.ts'],
      ['seeded', 'objectstack.seeded.config.ts'],
    ]) {
      await compileArtifact({
        frameworkRoot,
        packageRoot,
        stageRoot,
        configName,
        mode,
        frameworkSha,
        frameworkDirty,
        cliVersion,
      });
    }
  } finally {
    await fs.rm(stageRoot, { recursive: true, force: true });
  }
}

const isMain = process.argv[1]
  && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);

if (isMain) {
  try {
    const frameworkRoot = await validateFrameworkRoot(
      requiredFrameworkRoot(process.argv.slice(2)),
    );
    await buildArtifacts(frameworkRoot);
    console.log(JSON.stringify({ ok: true, frameworkRoot }));
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exitCode = 1;
  }
}
